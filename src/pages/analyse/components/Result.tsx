export function Malignant() {
  return (
    <div>
      <p>
        Malignant tumors have cells that grow uncontrollably and spread locally
        and/or to distant sites. Malignant tumors are cancerous (ie, they invade
        other sites). They spread to distant sites via the bloodstream or the
        lymphatic system. This spread is called metastasis. There are also
        general symptoms that people with cancerous tumors may experience,
        including:
      </p>
      <ul>
        <li>Fatigue.</li>
        <li>Shortness of breath.</li>
        <li>Anemia.</li>
        <li>Diarrhea.</li>
        <li>Weight loss.</li>
        <li>Drenching night sweats.</li>
        <li>Abnormal lumps or bumps.</li>
      </ul>
      <h3>Prevention of Malignant Cancer</h3>
      <ul>
        <li>Don&apos;t use tobacco.</li>
        <li>Eat a healthy diet.</li>
        <li>Maintain a healthy weight and be physically active.</li>
        <li>Protect yourself from the sun.</li>
        <li>Get vaccinated.</li>
        <li>Avoid risky behaviors.</li>
        <li>Get regular medical care.</li>
      </ul>
      <h3>Treatment of Malignant Cancer</h3>
      <p>
        Surgery: An operation where doctors cut out tissue with cancer cells.
        Chemotherapy: Special medicines that shrink or kill cancer cells.
        Radiation therapy: Using high-energy rays (similar to X-rays) to kill
        cancer cells. Hormone therapy: Blocks cancer cells from getting the
        hormones they need to grow.
      </p>
    </div>
  );
}
